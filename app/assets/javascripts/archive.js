// BORING ART 
$(document).ready(function() {
    $(".boringart").click(function() {
        $(this).css({
            "background-size": "175%"
        })
        $(this).click(function() {
            $(this).css({
                "background-size": "100%"
            })
            $(this).click(function() {
                $(this).css({
                    "background-size": "contain",
                    "background-position": "0%",
                    "height": "100%",
                    "width": "100%",
                    "cursor": "initial",
                    "background-repeat": "repeat-x"
                })


            });
        });
    });
});

